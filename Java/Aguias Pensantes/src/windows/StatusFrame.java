    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(txtBuscar.getText().isBlank() || txtBuscar.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "O campo para pesquisa por nome n√£o pode estar vazio!\n"
                                                           + "Por favor, digite seu nome para realizar a pesquisa.");
        }else{
        // Limpa a Tabela para receber os resultados
        tabela_ideias.removeDados();
        
        Dao ideiaDao = new Dao();
        // Obtendo os dados para pesquisa;
        ideiaDao.pesquisa_Ideia(txtBuscar.getText());
        // Preenchendo a tabela com os resultados da Pesquisa;
        tabela_ideias.preencheTabela(ideiaDao.pesquisa_Ideia(txtBuscar.getText()));
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

